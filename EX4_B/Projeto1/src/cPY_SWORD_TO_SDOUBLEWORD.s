.text
.global CPY_SWORD_TO_SDOUBLEWORD
.type CPY_SWORD_TO_SDOUBLEWORD,"function"


CPY_SWORD_TO_SDOUBLEWORD:


INIT:	MOV X9,#0 // VETOR DE BYTES
		MOV X10,#0 // VETOR DE DOUBLES

CICLO:	
		CBZ X2,FIM

		LDRSW X11,[X0,X9]
		
		ADDS X9,X9,#4		
		
		STR X11,[X1,X10]
		
		ADDS X10,X10,#8

		SUBS X2,X2,#1

		B CICLO		



FIM:	RET